ATTR_RE = re.compile(r'(\S+) "?([^";]+)"?;')


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build data/raw_data/tool_reference from public annotation inputs."
    )
    parser.add_argument(
        "--output-dir",
        default=None,
        help="Directory for generated references. Defaults to data/raw_data/tool_reference.",
    )
    parser.add_argument(
        "--cache-dir",
        default=None,
        help="Optional download cache for .gz source files. Defaults to <output-dir>/.cache.",
    )
    parser.add_argument(
        "--genome",
        choices=sorted(GENOMES),
        action="append",
        help="Genome to build. Repeatable. Defaults to both mm10 and hg38.",
    )
    parser.add_argument(
        "--scape-image",
        default=None,
        help="SCAPE SIF image. If provided, SCAPE prepare runs through apptainer/singularity.",
    )
    parser.add_argument(
        "--scape-repo",
        default=None,
        help="Local SCAPE checkout containing main.py. Used when --scape-image is not set.",
    )
    parser.add_argument(
        "--skip-scape",
        action="store_true",
        help="Build only GTF, chrom sizes, and scMAPA-compatible BED files.",
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Overwrite existing outputs.",
    )
    return parser.parse_args()


def script_dir() -> Path:
    return Path(__file__).resolve().parent


def resolve_path(path: str) -> Path:
    candidate = Path(path)
    if candidate.is_absolute():
        return candidate
    return candidate.resolve()


def md5(path: Path) -> str:
    return digest(path, "md5")


def sha256(path: Path) -> str:
    return digest(path, "sha256")


def digest(path: Path, algorithm: str) -> str:
    h = hashlib.new(algorithm)
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1024 * 1024), b""):
            h.update(block)
    return h.hexdigest()


def ensure_download(url: str, expected_md5: str, cache_dir: Path, force: bool) -> Path:
    cache_dir.mkdir(parents=True, exist_ok=True)
    target = cache_dir / url.rsplit("/", 1)[-1]
    if force or not target.exists():
        print(f"[download] {url}", file=sys.stderr)
        tmp = target.with_suffix(target.suffix + ".tmp")
        with urllib.request.urlopen(url) as response, tmp.open("wb") as out:
            shutil.copyfileobj(response, out)
        tmp.replace(target)
    observed = md5(target)
    if observed != expected_md5:
        raise RuntimeError(f"MD5 mismatch for {target}: expected {expected_md5}, observed {observed}")
    return target


def gunzip_to(source_gz: Path, target: Path, force: bool) -> None:
    if target.exists() and not force:
        return
    target.parent.mkdir(parents=True, exist_ok=True)
    tmp = target.with_suffix(target.suffix + ".tmp")
    with gzip.open(source_gz, "rb") as source, tmp.open("wb") as out:
        shutil.copyfileobj(source, out)
    tmp.replace(target)


def parse_attrs(raw_attrs: str) -> dict[str, str]:
    return dict(ATTR_RE.findall(raw_attrs))


def write_chrom_sizes(fasta_gz: Path, target: Path, force: bool) -> None:
    if target.exists() and not force:
        return
    tmp = target.with_suffix(target.suffix + ".tmp")
    with gzip.open(fasta_gz, "rt") as source, tmp.open("w") as out:
        name = None
        length = 0
        for line in source:
            if line.startswith(">"):
                if name is not None:
                    out.write(f"{name}\t{length}\n")
                name = line[1:].strip().split()[0]
                length = 0
            else:
                length += len(line.strip())
        if name is not None:
            out.write(f"{name}\t{length}\n")
    tmp.replace(target)


def terminal_exon_bed_from_gtf(gtf: Path, target: Path, force: bool) -> None:
    if target.exists() and not force:
        return
    transcript_meta: OrderedDict[str, tuple[str, str]] = OrderedDict()
    exons: dict[str, list[tuple[str, int, int, str, str]]] = defaultdict(list)
    with gtf.open() as handle:
        for line in handle:
            if line.startswith("#"):
                continue
            fields = line.rstrip("\n").split("\t")
            if len(fields) < 9:
                continue
            attrs = parse_attrs(fields[8])
            transcript_id = attrs.get("transcript_id")
            gene_id = attrs.get("gene_id")
            if not transcript_id or not gene_id:
                continue
            if fields[2] == "transcript" and 'tag "basic"' in fields[8]:
                transcript_meta[transcript_id] = (gene_id, fields[8])
            elif fields[2] == "exon":
                exons[transcript_id].append(
                    (fields[0], int(fields[3]), int(fields[4]), fields[6], gene_id)
                )

    intervals = []
    for transcript_id, (gene_id, _raw_attrs) in transcript_meta.items():
        transcript_exons = exons.get(transcript_id)
        if not transcript_exons:
            continue
        strand = transcript_exons[0][3]
        terminal = (
            max(transcript_exons, key=lambda item: item[2])
            if strand == "+"
            else min(transcript_exons, key=lambda item: item[1])
        )
        intervals.append((terminal[0], terminal[1], terminal[2], strand, transcript_id, gene_id))

    intervals.sort(key=lambda item: (item[0], item[1], item[2], item[3], item[4]))
    merged = []
    current = None
    for chrom, start, end, strand, transcript_id, gene_id in intervals:
        if current is None or chrom != current[0] or strand != current[3] or start > current[2]:
            if current is not None:
                merged.append(current)
            current = [chrom, start, end, strand, OrderedDict(), OrderedDict()]
        else:
            current[2] = max(current[2], end)
        current[4][transcript_id] = None
        current[5][gene_id] = None
    if current is not None:
        merged.append(current)

    tmp = target.with_suffix(target.suffix + ".tmp")
    with tmp.open("w") as out:
        for chrom, start, end, strand, transcript_ids, gene_ids in merged:
            name_parts = list(transcript_ids) + list(gene_ids) + [chrom, strand]
            out.write(f"{chrom}\t{start}\t{end}\t{'|'.join(name_parts)}\t0\t{strand}\n")
    tmp.replace(target)


def container_runtime() -> str | None:
    return shutil.which("apptainer") or shutil.which("singularity")


def run_scape_prepare(gtf: Path, output_dir: Path, label: str, args: argparse.Namespace) -> None:
    utr_target = output_dir / f"{label}.scape_utr.bed"
    intron_target = output_dir / f"{label}.scape_intron.bed"
    if utr_target.exists() and intron_target.exists() and not args.force:
        return
    if args.skip_scape:
        print(f"[skip] SCAPE references for {label}", file=sys.stderr)
        return

    with tempfile.TemporaryDirectory(prefix=f"{label}.scape.", dir=str(output_dir)) as tmp_dir_raw:
        tmp_dir = Path(tmp_dir_raw)
        prefix = tmp_dir / label
        if args.scape_image:
            runtime = container_runtime()
            if runtime is None:
                raise RuntimeError("--scape-image requires apptainer or singularity in PATH")
            image = Path(args.scape_image).resolve()
            command = [
                runtime,
                "exec",
                "--bind",
                f"{output_dir.resolve()}:{output_dir.resolve()}",
                "--bind",
                f"{tmp_dir.resolve()}:{tmp_dir.resolve()}",
                str(image),
                "python",
                "/scape/SCAPE/main.py",
                "prepare",
                "--gtf",
                str(gtf.resolve()),
                "--prefix",
                str(prefix.resolve()),
            ]
        elif args.scape_repo:
            scape_repo = Path(args.scape_repo).resolve()
            command = [
                sys.executable,
                str(scape_repo / "main.py"),
                "prepare",
                "--gtf",
                str(gtf.resolve()),
                "--prefix",
                str(prefix.resolve()),
            ]
        else:
            raise RuntimeError(
                "SCAPE reference generation requires --scape-image or --scape-repo. "
                "Use --skip-scape only for partial non-SCAPE preparation."
            )
        print(f"[scape] {' '.join(command)}", file=sys.stderr)
        subprocess.run(command, check=True)

        candidates = {
            utr_target: [tmp_dir / f"{label}_utr.bed", tmp_dir / f"{label}.utr.bed"],
            intron_target: [tmp_dir / f"{label}_intron.bed", tmp_dir / f"{label}.intron.bed"],
        }
        for target, paths in candidates.items():
            source = next((path for path in paths if path.exists()), None)
            if source is None:
                names = ", ".join(str(path) for path in paths)
                raise RuntimeError(f"SCAPE did not produce expected output for {target.name}: {names}")
            shutil.move(str(source), target)


def build_genome(genome: str, output_dir: Path, cache_dir: Path, args: argparse.Namespace) -> None:
    cfg = GENOMES[genome]
    gtf_gz = ensure_download(cfg["gtf_url"], cfg["gtf_md5"], cache_dir, args.force)
    fasta_gz = ensure_download(cfg["fasta_url"], cfg["fasta_md5"], cache_dir, args.force)

    gtf = output_dir / f"{cfg['label']}.annotation.gtf"
    gunzip_to(gtf_gz, gtf, args.force)
    write_chrom_sizes(fasta_gz, output_dir / cfg["chrom_sizes"], args.force)
    terminal_exon_bed_from_gtf(gtf, output_dir / cfg["scmapa_bed"], args.force)
    run_scape_prepare(gtf, output_dir, cfg["label"], args)


def main() -> int:
    args = parse_args()
    base = script_dir()
    output_dir = (
        (base / "../../data/raw_data/tool_reference").resolve()
        if args.output_dir is None
        else resolve_path(args.output_dir)
    )
    cache_dir = resolve_path(args.cache_dir) if args.cache_dir else output_dir / ".cache"
    output_dir.mkdir(parents=True, exist_ok=True)

    for genome in args.genome or sorted(GENOMES):
        print(f"[build] {genome}: {GENOMES[genome]['version']}", file=sys.stderr)
        build_genome(genome, output_dir, cache_dir, args)

    print("\nGenerated checksums:", file=sys.stderr)
    for path in sorted(output_dir.iterdir()):
        if path.is_file() and path.name != ".gitkeep":
            print(f"{path.name}\tsha256:{sha256(path)}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
