#!/usr/bin/env python3
"""Download and build public annotation references used by Stage 04.

The script keeps large payloads out of git while making the tool-reference
directory reproducible from public GENCODE inputs.  SCAPE UTR/intron BED files
are intentionally generated by the SCAPE official ``prepare`` command rather
than by a local reimplementation.
"""

from __future__ import annotations

import argparse
import gzip
import hashlib
import re
import shutil
import subprocess
import sys
import tempfile
import urllib.request
from collections import OrderedDict, defaultdict
from pathlib import Path


